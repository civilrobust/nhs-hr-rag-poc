# 7. IT Security & Acceptable Use
cat > it_security_acceptable_use_policy.txt << 'EOF'
NHS TRUST ICT DEPARTMENT
IT SECURITY & ACCEPTABLE USE POLICY
Policy Reference: HR-ICT-017
Effective Date: January 2024

1. PASSWORD REQUIREMENTS
Minimum 12 characters
Combination of upper, lower, numbers, special characters
Changed every 90 days
Never shared or written down
Different for each system

2. DATA PROTECTION
Patient data: Only access if required for role
Encryption: Mandatory for all removable media
Email: Do not send patient data without encryption
Printing: Collect immediately, do not leave sensitive documents

3. ACCEPTABLE USE
Personal use: Reasonable during breaks only
Prohibited: Illegal downloads, streaming, gambling, adult content
Social media: No Trust business on personal accounts without approval
Email: Professional tone, Trust disclaimer applies

4. SECURITY INCIDENTS
Report immediately: Lost devices, suspected breach, phishing emails
Contact: IT Security team via extension 2222
Timeline: Within 2 hours of discovery
Consequences: Disciplinary action for unreported incidents

5. REMOTE WORKING SECURITY
VPN mandatory for all external access
Public WiFi: Avoid for sensitive work, use VPN if essential
Home network: WPA2 encryption minimum, strong password
Device security: Lock screen when unattended, store securely

Contact: itsecurity@nhstrust.nhs.uk
EOF

# 8. Recruitment Policy
cat > recruitment_policy.txt << 'EOF'
NHS TRUST ICT DEPARTMENT
RECRUITMENT POLICY
Policy Reference: HR-ICT-018
Effective Date: January 2024

1. VACANCY APPROVAL
All vacancies require Director of Digital approval
Business case: Justify need, budget confirmed
Job description: Up-to-date, banding verified

2. ADVERTISING
NHS Jobs: Minimum 2 weeks posting
External sites: LinkedIn, Indeed (for specialist roles)
Internal opportunities: Advertised to existing staff first (5 days)

3. SELECTION PROCESS
Shortlisting: Minimum 2 panel members, criteria-based
Interviews: Structured questions, competency-based
Assessment: Technical test for technical roles (1 hour maximum)
Panel: Minimum 2 interviewers, diversity where possible

4. PRE-EMPLOYMENT CHECKS
DBS: Standard DBS for all ICT staff (6-week processing)
References: 2 references covering last 3 years employment
Right to work: Original documents verified
Occupational health: Clearance required before start date
Qualifications: Certificates verified for essential qualifications

5. OFFERS
Conditional offer: Subject to satisfactory pre-employment checks
Start date: Minimum 4 weeks notice period (negotiate if needed)
Contract: Issued within 1 week of unconditional offer

6. ONBOARDING
IT account creation: 5 days before start date
Equipment ordered: 2 weeks before start date
Induction schedule: First week planned
Buddy assigned: Experienced team member

Contact: ict.recruitment@nhstrust.nhs.uk
EOF

# 9. Redundancy & Restructuring
