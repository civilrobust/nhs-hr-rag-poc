# 5. Working Hours Policy
cat > working_hours_policy.txt << 'EOF'
NHS TRUST ICT DEPARTMENT
WORKING HOURS & FLEXITIME POLICY
Policy Reference: HR-ICT-015
Effective Date: January 2024

1. STANDARD WORKING HOURS
Full-time: 37.5 hours per week
Core hours: 10:00-15:00 (must be present/available)
Flexible hours: 07:00-10:00 and 15:00-19:00

2. FLEXITIME SYSTEM
Accrual: Track hours via ESR system
Carry forward: Maximum 7.5 hours per month
Flexi-leave: Can take up to 1 day per month using accrued hours
Deficit: Maximum -3.75 hours (must clear within next month)

3. BREAKS
Lunch: Minimum 30 minutes unpaid (not part of 37.5 hours)
Rest breaks: 15 minutes per 4 hours worked (paid)
Screen breaks: Recommended 5 minutes per hour for DSE users

4. OVERTIME
Pre-authorization required for all overtime
Rates: Time-and-a-half (weekdays), Double-time (weekends/bank holidays)
Time off in lieu (TOIL) alternative available

Contact: ict.hr@nhstrust.nhs.uk
EOF

