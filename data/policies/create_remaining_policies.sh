3. ACTING UP OPPORTUNITIES
Cover for vacant post: Minimum 4 weeks duration
Acting up allowance: Difference between bands (pro-rata if partial duties)
Development opportunity: Builds experience for promotion
No automatic promotion: Must apply through normal process

4. DEVELOPMENT PATHWAYS
Technical route: Specialist/expert (Bands 5-7)
Management route: Team leader to manager (Bands 6-8)
Hybrid route: Technical leadership (Bands 6-8)
Project route: Project manager to programme manager (Bands 6-8)

5. SECONDMENTS
Internal secondments: 6-12 months typical
External secondments: NHS Digital, other Trusts
Development opportunity: Broaden experience
Return to substantive post: Guaranteed at end of secondment

6. CAREER CONVERSATIONS
Annual appraisal: Discuss career aspirations
Personal development plan: Identify development needs
Mentor support: Available for career guidance
Talent management: High performers identified for development

Contact: ict.careers@nhstrust.nhs.uk
EOF

