NHS TRUST ICT DEPARTMENT
REDUNDANCY & RESTRUCTURING POLICY
Policy Reference: HR-ICT-019
Effective Date: January 2024

1. CONSULTATION PERIOD
Individual redundancy: Minimum 30 days consultation
20+ redundancies: Minimum 45 days consultation
100+ redundancies: Minimum 90 days consultation

2. SELECTION CRITERIA
Skills and qualifications
Performance and conduct record
Experience and length of service
Attendance record
Scored objectively, moderation by HR

3. REDEPLOYMENT
Priority for suitable alternative posts within Trust
4-week trial period in new role
Training provided if skill gaps identified
Salary protection for 12 months if lower grade

4. REDUNDANCY PAY
Statutory redundancy pay minimum
Trust policy: Enhanced redundancy (up to 18 months service counted)
Calculation: Week's pay per year of service
Maximum: 30 weeks pay (Trust scheme)

5. NOTICE PERIODS
Minimum statutory notice
Contractual notice: As per contract (typically 1-3 months)
Payment in lieu available (PILON)

6. SUPPORT
Career coaching available
CV and interview skills workshops
Job search facilities and time off for interviews
Access to Employee Assistance Programme

Contact: ict.hr@nhstrust.nhs.uk
EOF

# 10. Career Progression Policy
